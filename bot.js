bot.action(/^na/, async (ctx) => {
  const data = ctx.callbackQuery?.data;
  const from = ctx.from?.id;

  if (!data || !from) {
    await ctx.answerCbQuery().catch(() => {});
    return;
  }

  const parts = data.split(":");
  if (parts.length < 4) {
    await ctx.answerCbQuery("âš ï¸ Malformed action data.").catch(() => {});
    return;
  }

  const prefix = parts[0];
  const round = parts[1];
  const actorId = parts[2];
  const value = parts.slice(3).join(":");

  if (String(from) !== actorId) {
    await ctx.answerCbQuery("âš ï¸ This isn't your prompt.").catch(() => {});
    return;
  }

  if (!gameState.players) {
    await ctx
      .answerCbQuery("âš ï¸ Game state not ready.", { show_alert: true })
      .catch(() => {});
    return;
  }

  const key = `${prefix}:${round}:${actorId}`;
  const resolved = actionRegistry.resolve(key, value);

  if (resolved) {
    const player = gameState.players.get(from);
    log("NIGHT", `${player?.username ?? from} submitted their night action`);
    await ctx.answerCbQuery("âœ… Action recorded!").catch(() => {});
    await ctx.editMessageReplyMarkup({ inline_keyboard: [] }).catch(() => {});
  } else {
    warn(
      "NIGHT",
      `${gameState.players.get(from)?.username ?? from} pressed a button that is no longer valid`,
    );
    await ctx
      .answerCbQuery(
        "âš ï¸ This action is no longer valid. The game may have been reset.",
        { show_alert: true },
      )
      .catch(() => {});
  }
});

